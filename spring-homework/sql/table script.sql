CREATE TABLE BOARD(
    NO NUMBER PRIMARY KEY,
    TITLE VARCHAR2(400) NOT NULL,
    SUB_TITLE VARCHAR2(200) ,
    CONTENT VARCHAR2(4000) NOT NULL,
    CREATE_DATE DATE DEFAULT SYSDATE
   -- ,USER_NO NUMBER REFERENCES MEMBER
