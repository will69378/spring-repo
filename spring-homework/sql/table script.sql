CREATE TABLE BOARD_IMG(
    BOARD_IMG_NO NUMBER PRIMARY KEY,
    UPLOAD_PATH VARCHAR2(200) NOT NULL,
    ORIGIN_NAME VARCHAR2(200) NOT NULL,
    CHANGE_NAME VARCHAR2(200) NOT NULL,
    REF_NO NUMBER REFERENCES BOARD
);

